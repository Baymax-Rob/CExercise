// 计算存款利息。例如：有10000元，想存一年，有3种方法可以选择：
// (1) 活期，年利率为r1; (2) 一年定期，年利率为r2; (3)存两次半年定期，年利率为r3。
// 请分别计算出一年后按以上3种方法获得的本息和

#include <stdio.h>
int main(void)
{
  float p0 = 10000, r1 = 0.0028, r2 = 0.0228, r3 = 0.0118;
  float p1, p2, p3;
  
  p1 = p0 * (1 + r1);
  p2 = p0 * (1 + r2);
  p3 = p0 * (1 + r3 / 2) * (1 + r3 / 2);
  printf("p1 = %f\n p2 = %f\n p3 = %f", p1, p2, p3);
  
  return 0;
}
